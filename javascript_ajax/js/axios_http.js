import axios from "axios";

axios
    .get('https://httpbin.org/ip')
    .then((res) => {
        console.log('Data =>', res.data);
    })
    .catch((err) => {
        console.warn(err);
    });

axios
    .post('https://httpbin.org/post', {
        name: 'Leonan',
        age: 21,
    })
    .then((res) => {
        console.log('Response =>', res);
        console.log('Data 2 =>', res.data);
    })
    .catch((err) => {
        console.warn(err);
    });